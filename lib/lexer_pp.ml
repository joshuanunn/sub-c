let show_token = function
  | Parser.IDENTIFIER s -> "IDENTIFIER(" ^ s ^ ")"
  | Parser.LITERAL_INT i -> "LITERAL_INT(" ^ string_of_int i ^ ")"
  | Parser.KW_INT -> "KW_INT"
  | Parser.KW_VOID -> "KW_VOID"
  | Parser.KW_RETURN -> "KW_RETURN"
  | Parser.KW_IF -> "KW_IF"
  | Parser.KW_ELSE -> "KW_ELSE"
  | Parser.KW_DO -> "KW_DO"
  | Parser.KW_WHILE -> "KW_WHILE"
  | Parser.KW_FOR -> "KW_FOR"
  | Parser.KW_BREAK -> "KW_BREAK"
  | Parser.KW_CONTINUE -> "KW_CONTINUE"
  | Parser.KW_SWITCH -> "KW_SWITCH"
  | Parser.KW_CASE -> "KW_CASE"
  | Parser.KW_DEFAULT -> "KW_DEFAULT"
  | Parser.KW_GOTO -> "KW_GOTO"
  | Parser.LPAREN -> "LPAREN"
  | Parser.RPAREN -> "RPAREN"
  | Parser.LBRACE -> "LBRACE"
  | Parser.RBRACE -> "RBRACE"
  | Parser.SEMICOLON -> "SEMICOLON"
  | Parser.QUESTION -> "QUESTION"
  | Parser.COLON -> "COLON"
  | Parser.COMMA -> "COMMA"
  | Parser.INCREMENT -> "INCREMENT"
  | Parser.DECREMENT -> "DECREMENT"
  | Parser.ADD_ASSIGN -> "ADD_ASSIGN"
  | Parser.SUB_ASSIGN -> "SUB_ASSIGN"
  | Parser.MUL_ASSIGN -> "MUL_ASSIGN"
  | Parser.DIV_ASSIGN -> "DIV_ASSIGN"
  | Parser.MOD_ASSIGN -> "MOD_ASSIGN"
  | Parser.LSHIFT_ASSIGN -> "LSHIFT_ASSIGN"
  | Parser.RSHIFT_ASSIGN -> "RSHIFT_ASSIGN"
  | Parser.BW_AND_ASSIGN -> "BW_AND_ASSIGN"
  | Parser.BW_OR_ASSIGN -> "BW_OR_ASSIGN"
  | Parser.BW_XOR_ASSIGN -> "BW_XOR_ASSIGN"
  | Parser.ADD -> "ADD"
  | Parser.SUB -> "SUB"
  | Parser.MUL -> "MUL"
  | Parser.DIV -> "DIV"
  | Parser.MOD -> "MOD"
  | Parser.AND -> "AND"
  | Parser.OR -> "OR"
  | Parser.BW_LSHIFT -> "BW_LSHIFT"
  | Parser.BW_RSHIFT -> "BW_RSHIFT"
  | Parser.BW_NOT -> "BW_NOT"
  | Parser.BW_AND -> "BW_AND"
  | Parser.BW_OR -> "BW_OR"
  | Parser.BW_XOR -> "BW_XOR"
  | Parser.EQ -> "EQ"
  | Parser.NE -> "NE"
  | Parser.LE -> "LE"
  | Parser.GE -> "GE"
  | Parser.LT -> "LT"
  | Parser.GT -> "GT"
  | Parser.NOT -> "NOT"
  | Parser.ASSIGN -> "ASSIGN"
  | Parser.EOF -> "EOF"
