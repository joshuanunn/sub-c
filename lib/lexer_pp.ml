let show_token = function
  | Parser.IDENTIFIER s -> "(Token.IDENTIFIER \"" ^ s ^ "\")"
  | Parser.LITERAL_INT i -> "(Token.LITERAL_INT " ^ string_of_int i ^ ")"
  | Parser.KW_INT -> "Token.KW_INT"
  | Parser.KW_VOID -> "Token.KW_VOID"
  | Parser.KW_RETURN -> "Token.KW_RETURN"
  | Parser.KW_IF -> "Token.KW_IF"
  | Parser.KW_ELSE -> "Token.KW_ELSE"
  | Parser.KW_DO -> "Token.KW_DO"
  | Parser.KW_WHILE -> "Token.KW_WHILE"
  | Parser.KW_FOR -> "Token.KW_FOR"
  | Parser.KW_BREAK -> "Token.KW_BREAK"
  | Parser.KW_CONTINUE -> "Token.KW_CONTINUE"
  | Parser.KW_SWITCH -> "Token.KW_SWITCH"
  | Parser.KW_CASE -> "Token.KW_CASE"
  | Parser.KW_DEFAULT -> "Token.KW_DEFAULT"
  | Parser.KW_GOTO -> "Token.KW_GOTO"
  | Parser.LPAREN -> "Token.LPAREN"
  | Parser.RPAREN -> "Token.RPAREN"
  | Parser.LBRACE -> "Token.LBRACE"
  | Parser.RBRACE -> "Token.RBRACE"
  | Parser.SEMICOLON -> "Token.SEMICOLON"
  | Parser.QUESTION -> "Token.QUESTION"
  | Parser.COLON -> "Token.COLON"
  | Parser.COMMA -> "Token.COMMA"
  | Parser.INCREMENT -> "Token.INCREMENT"
  | Parser.DECREMENT -> "Token.DECREMENT"
  | Parser.ADD_ASSIGN -> "Token.ADD_ASSIGN"
  | Parser.SUB_ASSIGN -> "Token.SUB_ASSIGN"
  | Parser.MUL_ASSIGN -> "Token.MUL_ASSIGN"
  | Parser.DIV_ASSIGN -> "Token.DIV_ASSIGN"
  | Parser.MOD_ASSIGN -> "Token.MOD_ASSIGN"
  | Parser.LSHIFT_ASSIGN -> "Token.LSHIFT_ASSIGN"
  | Parser.RSHIFT_ASSIGN -> "Token.RSHIFT_ASSIGN"
  | Parser.BW_AND_ASSIGN -> "Token.BW_AND_ASSIGN"
  | Parser.BW_OR_ASSIGN -> "Token.BW_OR_ASSIGN"
  | Parser.BW_XOR_ASSIGN -> "Token.BW_XOR_ASSIGN"
  | Parser.ADD -> "Token.ADD"
  | Parser.SUB -> "Token.SUB"
  | Parser.MUL -> "Token.MUL"
  | Parser.DIV -> "Token.DIV"
  | Parser.MOD -> "Token.MOD"
  | Parser.AND -> "Token.AND"
  | Parser.OR -> "Token.OR"
  | Parser.BW_LSHIFT -> "Token.BW_LSHIFT"
  | Parser.BW_RSHIFT -> "Token.BW_RSHIFT"
  | Parser.BW_NOT -> "Token.BW_NOT"
  | Parser.BW_AND -> "Token.BW_AND"
  | Parser.BW_OR -> "Token.BW_OR"
  | Parser.BW_XOR -> "Token.BW_XOR"
  | Parser.EQ -> "Token.EQ"
  | Parser.NE -> "Token.NE"
  | Parser.LE -> "Token.LE"
  | Parser.GE -> "Token.GE"
  | Parser.LT -> "Token.LT"
  | Parser.GT -> "Token.GT"
  | Parser.NOT -> "Token.NOT"
  | Parser.ASSIGN -> "Token.ASSIGN"
  | Parser.EOF -> "Token.EOF"
